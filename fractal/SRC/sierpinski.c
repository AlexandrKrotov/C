#include "fractal.h"

void	serpinski(t_all *all)
{
	int	y;
	int	x;

	y = 0;
	while (y < all->disp.d_height)
	{
		x = 0;
		while (x < all->disp.d_width)
		{
			if (!(((x) / 1) % 3 == 1 &&
			(y / 1) % 3 == 1) &&
			!((x / 3) % 3 == 1 &&
			(y / 3) % 3 == 1) &&
			!((x / 9) % 3 == 1 &&
			(y / 9) % 3 == 1) &&
			!((x / 27) % 3 == 1 &&
			(y / 27) % 3 == 1) &&
			!((x / 81) % 3 == 1 &&
			(y / 81) % 3 == 1))
				ft_put_color(all, x, y, (0xffffff + x / 5 + 2));
			x++;
		}
		y++;
	}
}
